package net.tot3g.omclient.model;

public class APN {
	
	private int apnId;
	private String apnName;
	private String apnArea;
	private String qosProfile;
	
	public int getApnId() {
		return apnId;
	}
	public void setApnId(int apnId) {
		this.apnId = apnId;
	}
	public String getApnName() {
		return apnName;
	}
	public void setApnName(String apnName) {
		this.apnName = apnName;
	}
	public String getApnArea() {
		return apnArea;
	}
	public void setApnArea(String apnArea) {
		this.apnArea = apnArea;
	}
	public String getQosProfile() {
		return qosProfile;
	}
	public void setQosProfile(String qosProfile) {
		this.qosProfile = qosProfile;
	}

}
